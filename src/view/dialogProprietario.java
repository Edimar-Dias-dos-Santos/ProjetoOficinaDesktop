    //3-monta um objeto com valores da tela
    private Proprietario createProprietario(){
        return new Proprietario(
                textCodigo.getText().isEmpty()? 0 : Integer.parseInt(textCodigo.getText()), 
                textNome.getText(),
                LocalDate.parse(textNascimento.getText(),formato),
                textCpf.getText(),
                textEndereco.getText(),
                (Cidade)comboCidade.getSelectedItem()
        );
    }
    //4-atualiza componentes de tela
    private void fillComponents(Proprietario proprietario){
        textCodigo.setText(proprietario.getId()+"");
        textNome.setText(proprietario.getNome());
        textNascimento.setText(proprietario.getNascimento().format(formato));
        textCpf.setText(proprietario.getCpf());
        textEndereco.setText(proprietario.getEndereco());
        comboCidade.setSelectedItem(proprietario.getCidade());
    }
    //5-carregar combo cidade
    private void loadComboCidade(){
        DefaultComboBoxModel cbm = 
        new DefaultComboBoxModel(new daoCidade().read().toArray());
        comboCidade.setModel(cbm);
    }
    //6-atualizar List
    private void loadList(){
        DefaultListModel lm = new DefaultListModel();
        lm.addAll(dao.read());
        listProprietario.setModel(lm);        
    }
    private void loadList(String filtro){
        DefaultListModel lm = new DefaultListModel();
        lm.addAll(dao.read(filtro));
        listProprietario.setModel(lm);        
    }

